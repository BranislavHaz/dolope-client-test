import { useEffect } from "react";
import Head from "next/head";

import * as $ from "./index.styled";
import useWardrobeStore from "@/stores/wardrobeStore";

import Stand from "@/components/elements/stand";
import Module1 from "@/components/modules/module1";
import Module2 from "@/components/modules/module2";
import Module3 from "@/components/modules/module3";
import Module4 from "@/components/modules/Module4";
import InputSelect from "@/components/modules/inputSelect";

const MODULE_MAP = {
  1: Module1,
  2: Module2,
  3: Module3,
  4: Module4,
};

const index = () => {
  const { sections, width, height, setViewportSize, mm } = useWardrobeStore(
    (state) => ({
      width: state.width,
      height: state.height,
      setViewportSize: state.setViewportSize,
      mm: state.mm,
      sections: state.sections,
    })
  );

  useEffect(() => {
    setViewportSize({
      width: window.innerWidth,
      height: window.innerHeight,
    });

    const handleResize = () => {
      setViewportSize({
        width: window.innerWidth,
        height: window.innerHeight,
      });
    };

    window.addEventListener("resize", handleResize);

    return () => {
      window.removeEventListener("resize", handleResize);
    };
  }, []);

  const getModule = (sectionId) => {
    const moduleId = sections[sectionId];
    const FristModul = MODULE_MAP[1];
    const Modul = MODULE_MAP[moduleId];

    if (moduleId === 0) {
      return <FristModul />;
    }
    return <Modul />;
  };

  return (
    <>
      <Head>
        <title>Nakonfigurujte si vlastnu skri≈àu</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <>
        <$.Main>
          <$.Wardrobe
            $width={mm * width}
            $height={mm * height}
            key={Math.random()}
          >
            <Stand />
            {getModule(1)}
            <Stand />
            {getModule(2)}
            <Stand />
            {getModule(3)}
            <Stand />
          </$.Wardrobe>
        </$.Main>
      </>
      <$.Inputs>
        <InputSelect id={1} />
        <InputSelect id={2} />
        <InputSelect id={3} />
      </$.Inputs>
    </>
  );
};

export default index;
